# while : tant que une condition est vrai
# salarié 1500€ / mois
salary = 1500

# tant que ce salarié a un salaire de < 2000€ alors il aura + 120€ d'aide de finances

while salary < 2000 :
    # ajouter 120€ au salaire
    salary += 120
    # afficher le résultat
    print("Votre salaire actuelle est de : ", salary, "€")
print("Fin du programme")